// Alternative with explicit return statements
function add(a, b) {
  let sum = a + b;
  return sum;
}

function subtract(a, b) {
  let difference = a - b;
  return difference;
}

function multiply(a, b) {
  let product = a * b;
  return product;
}

function divide(a, b) {
  let quotient = a / b;
  return quotient;
}

function increment(n) {
  let result = n + 1;
  return result;
}

function decrement(n) {
  let result = n - 1;
  return result;
}

function makeInt(n) {
  let integer = parseInt(n, 10);
  return integer;
}

function preserveDecimal(n) {
  let decimal = parseFloat(n);
  return decimal;
}